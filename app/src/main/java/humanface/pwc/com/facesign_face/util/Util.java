    public static String getFacesetToken(Context context){
        SharedPreferencesHelper helper =  new SharedPreferencesHelper(context,"FaceSign");
        String facesetToken = helper.getValue("FacesetToken");
        return facesetToken;
    }
    public static void setFacesetToken(Context context,String faceset_token){
        if(TextUtils.isEmpty(faceset_token)){
            return;
        }
        SharedPreferencesHelper helper =  new SharedPreferencesHelper(context,"FaceSign");
        helper.putValue("FacesetToken",faceset_token);
    }
